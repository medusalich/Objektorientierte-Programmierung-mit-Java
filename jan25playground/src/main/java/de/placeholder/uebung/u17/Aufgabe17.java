public class Aufgabe17 {

    /*
    Aufgabe 1
    Schreibe ein kleines Programm.
    Das Programm fragt eine Eingabe von Konsole ab und schreibt diese als String in eine Liste.
    Danach wird die Liste in einer Datei gespeichert und der Inhalt angezeigt.

    Beim Neustart des Progamms sollen Altdaten angezeigt werden.
    */

    /*
    Aufgabe 2
    Schreibe ein kleines Programm.
    Das Programm fordert dich auf einen Dateinamen einzugeben.
    Danach wirst du gefragt, ob du in die Datei schreiben, oder die Datei analysieren willst.

    Beim Schreiben kannst du einen freien Text eingeben, der in der Datei gespeichert wird.

    Beim Analysieren kannst du ein Wort eingeben, welches in dem Text gesucht wird.
    Anschließend bekommst du angezeigt, wie oft das Wort in der Datei vorkommt.
    Bei der Suche soll nicht zwischen Groß- und Kleinschreibung unterschieden werden.

    Lösungsansatz: Lese den Inhalt Zeilenweise und zerlege dann die Zeile in einzelne Wörter.
    Erst dann zähle das Vorkommen in dieser Zeile :-)
     */

    /*
    Aufgabe 3
    Schreibe ein kleines Programm.
    Das Programm fordert dich auf eine Zahl einzugeben.
    Wird statt einer Zahl ein Buchstabe oder Wort eingegeben wird normalerweise eine Exception geworfen.
    Behandle diese Exception so, dass stattdessen die sinnvolle Meldung erfolgt und der
    Benutzer eine neue Zahl eingeben kann.
     */
}
